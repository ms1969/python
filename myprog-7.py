# –ú–æ–µ —Ä–µ—à–µ–Ω–∏–µ

n = int(input())
k = int(input())
m = [i for i in range(1, n + 1)]
lposition = 1
while len(m) > 1:
    for i in range(1, k + 1):
        if i == k:
            m.remove(m[lposition - 1])
        else:
            lposition += 1
        if lposition > len(m):
            lposition = 1
print(m[0])

